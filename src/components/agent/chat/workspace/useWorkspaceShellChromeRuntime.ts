import { useMemo } from "react";
import {
  resolveWorkspaceShellChromeRuntime,
  type ResolveWorkspaceShellChromeRuntimeParams,
} from "./workspaceShellChromeRuntime";

export function useWorkspaceShellChromeRuntime({
  activeTheme,
  agentEntry,
  contextWorkspaceEnabled,
  effectiveShowChatPanel,
  gateStatus,
  generalWorkbenchPanelCollapseEnabled,
  generalWorkbenchSidebarCollapsed,
  hasCanvasWorkbenchContent,
  hasDisplayMessages,
  hasHomeConversationActivity,
  hasPendingA2UIForm,
  hideTopBar,
  isBootstrapDispatchPending,
  isPreparingSend,
  isSending,
  isTaskCenterDraftSendPending,
  isThemeWorkbench,
  layoutMode,
  normalizedInitialSessionId,
  queuedTurnCount,
  sessionId,
  shouldRenderTaskCenterEmbeddedHome,
  shouldSuppressTaskCenterDraftContent,
  shouldUseBrowserWorkspaceHomeChrome,
  shouldUseCompactGeneralWorkbench,
  showSidebar,
  subagentsRuntimeVisible,
  hasRuntimeSessions,
  hasTeamDispatchPreview,
  themeWorkbenchRunState,
  topBarChrome,
}: ResolveWorkspaceShellChromeRuntimeParams) {
  return useMemo(
    () =>
      resolveWorkspaceShellChromeRuntime({
        activeTheme,
        agentEntry,
        contextWorkspaceEnabled,
        effectiveShowChatPanel,
        gateStatus,
        generalWorkbenchPanelCollapseEnabled,
        generalWorkbenchSidebarCollapsed,
        hasCanvasWorkbenchContent,
        hasDisplayMessages,
        hasHomeConversationActivity,
        hasPendingA2UIForm,
        hideTopBar,
        isBootstrapDispatchPending,
        isPreparingSend,
        isSending,
        isTaskCenterDraftSendPending,
        isThemeWorkbench,
        layoutMode,
        normalizedInitialSessionId,
        queuedTurnCount,
        sessionId,
        shouldRenderTaskCenterEmbeddedHome,
        shouldSuppressTaskCenterDraftContent,
        shouldUseBrowserWorkspaceHomeChrome,
        shouldUseCompactGeneralWorkbench,
        showSidebar,
        subagentsRuntimeVisible,
        hasRuntimeSessions,
        hasTeamDispatchPreview,
        themeWorkbenchRunState,
        topBarChrome,
      }),
    [
      activeTheme,
      agentEntry,
      contextWorkspaceEnabled,
      effectiveShowChatPanel,
      gateStatus,
      generalWorkbenchPanelCollapseEnabled,
      generalWorkbenchSidebarCollapsed,
      hasCanvasWorkbenchContent,
      hasDisplayMessages,
      hasHomeConversationActivity,
      hasPendingA2UIForm,
      hideTopBar,
      isBootstrapDispatchPending,
      isPreparingSend,
      isSending,
      isTaskCenterDraftSendPending,
      isThemeWorkbench,
      layoutMode,
      normalizedInitialSessionId,
      queuedTurnCount,
      sessionId,
      shouldRenderTaskCenterEmbeddedHome,
      shouldSuppressTaskCenterDraftContent,
      shouldUseBrowserWorkspaceHomeChrome,
      shouldUseCompactGeneralWorkbench,
      showSidebar,
      subagentsRuntimeVisible,
      hasRuntimeSessions,
      hasTeamDispatchPreview,
      themeWorkbenchRunState,
      topBarChrome,
    ],
  );
}
