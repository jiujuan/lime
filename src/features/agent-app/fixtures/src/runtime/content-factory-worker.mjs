import { resolve } from "node:path";
import { fileURLToPath } from "node:url";

export const WORKER_REQUEST_SCHEMA = "content-factory.worker-request.v1";
export const WORKER_RESPONSE_SCHEMA = "content-factory.worker-response.v1";
export const PRODUCT_WORKSPACE_SCHEMA = "product-workspace.v1";
export const CONTENT_FACTORY_APP_ID = "content-factory-app";
export const WORKSPACE_PATCH_KIND = "content_factory.workspace_patch";

const SUPPORTED_TASK_KINDS = new Set([
  "content.factory.generate",
  "content.article.generate",
  "content.image.generate",
  "content.video.script.generate",
  "content.video.storyboard.generate",
  "content.delivery.review",
]);

function asRecord(value) {
  return value && typeof value === "object" && !Array.isArray(value)
    ? value
    : null;
}

function readString(value) {
  return typeof value === "string" && value.trim() ? value.trim() : null;
}

function normalizeRequest(input) {
  const request = asRecord(input);
  if (!request) {
    return { error: "WORKER_REQUEST_INVALID" };
  }
  const schemaVersion = readString(request.schemaVersion);
  const appId = readString(request.appId);
  const sessionId = readString(request.sessionId);
  const turnId = readString(request.turnId);
  const taskId = readString(request.taskId);
  const taskKind = readString(request.taskKind);
  const prompt = readString(request.prompt);
  const runtime = asRecord(request.runtime);
  const expectedOutput = asRecord(request.expectedOutput);
  if (
    schemaVersion !== WORKER_REQUEST_SCHEMA ||
    appId !== CONTENT_FACTORY_APP_ID ||
    !sessionId ||
    !turnId ||
    !taskId ||
    !taskKind ||
    !prompt
  ) {
    return { error: "WORKER_REQUEST_REQUIRED_FIELD_MISSING" };
  }
  if (!SUPPORTED_TASK_KINDS.has(taskKind)) {
    return { error: "WORKER_TASK_KIND_UNSUPPORTED" };
  }
  if (
    runtime?.directProviderAccess !== false ||
    runtime?.directFilesystemAccess !== false ||
    readString(runtime?.outputArtifactKind) !== WORKSPACE_PATCH_KIND ||
    readString(expectedOutput?.artifactKind) !== WORKSPACE_PATCH_KIND
  ) {
    return { error: "WORKER_RUNTIME_CONTRACT_UNSUPPORTED" };
  }
  return {
    request: {
      actionKey: readString(request.actionKey),
      prompt,
      requestedAt: readString(request.requestedAt),
      sessionId,
      sourceObjectRef: asRecord(request.sourceObjectRef),
      taskId,
      taskKind,
      turnId,
      workspaceId: readString(request.workspaceId),
    },
  };
}

function objectRef(request, kind, id, artifactIds = []) {
  return {
    appId: CONTENT_FACTORY_APP_ID,
    kind,
    id,
    sessionId: request.sessionId,
    artifactIds,
    sourceTurnId: request.turnId,
    sourceTaskId: request.taskId,
  };
}

