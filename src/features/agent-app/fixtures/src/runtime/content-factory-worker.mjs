function selectedObjectRef(request, objects) {
  const requestedKind = readString(request.sourceObjectRef?.kind);
  const requestedId = readString(request.sourceObjectRef?.id);
  const selected =
    objects.find(
      (object) =>
        object.ref.kind === requestedKind &&
        (!requestedId || object.ref.id === requestedId),
    ) ??
    objects.find((object) => object.ref.kind === "articleDraft") ??
    objects[0];
  return selected?.ref ?? null;
}

export function buildContentFactoryWorkspacePatch(request) {
  const objects = buildObjects(request);
  const selected = selectedObjectRef(request, objects);
  return {
    schemaVersion: PRODUCT_WORKSPACE_SCHEMA,
    kind: WORKSPACE_PATCH_KIND,
    appId: CONTENT_FACTORY_APP_ID,
    sessionId: request.sessionId,
    workspaceId: request.workspaceId,
    selectedObjectRef: selected,
    primaryObjectRef:
      objects.find((object) => object.ref.kind === "articleDraft")?.ref ??
      selected,
    objects,
    layoutState: {
      activeTabKind: "productProfile",
      activePaneKind:
        selected?.kind === "imageGenerationSet"
          ? "imageGrid"
          : "documentCanvas",
      openTabKinds: ["productProfile"],
      splitMode: null,
    },
    sourceArtifacts: [
      {
        source: "content_factory_worker",
        artifactRef: `${request.taskId}:workspace-patch`,
        taskKind: request.taskKind,
        turnId: request.turnId,
      },
    ],
    workerEvidence: [
      {
        source: "worker",
        taskId: request.taskId,
        turnId: request.turnId,
        taskKind: request.taskKind,
        status: "completed",
        artifactKind: WORKSPACE_PATCH_KIND,
      },
    ],
    actionHistory: request.actionKey
      ? [
          {
            actionKey: request.actionKey,
            objectRef: request.sourceObjectRef,
            taskId: request.taskId,
            turnId: request.turnId,
            status: "completed",
            resultArtifacts: [`${request.taskId}:workspace-patch`],
          },
        ]
      : [],
    updatedAt: request.requestedAt,
  };
}

export function handleContentFactoryWorkerRequest(input) {
  const normalized = normalizeRequest(input);
  if (normalized.error) {
    return {
      schemaVersion: WORKER_RESPONSE_SCHEMA,
      appId: CONTENT_FACTORY_APP_ID,
      status: "failed",
      error: {
        code: normalized.error,
      },
      artifacts: [],
    };
  }

  const { request } = normalized;
  const workspacePatch = buildContentFactoryWorkspacePatch(request);
  return {
    schemaVersion: WORKER_RESPONSE_SCHEMA,
    appId: CONTENT_FACTORY_APP_ID,
    sessionId: request.sessionId,
    turnId: request.turnId,
    taskId: request.taskId,
    taskKind: request.taskKind,
    status: "completed",
    artifacts: [
      {
        kind: "artifact.snapshot",
        artifactId: `${request.taskId}:workspace-patch`,
        title: "Content Factory workspace patch",
        contentType: "application/json",
        metadata: {
          kind: WORKSPACE_PATCH_KIND,
          contentFactoryWorkspacePatch: workspacePatch,
          workspace_patch: workspacePatch,
        },
        content: JSON.stringify(workspacePatch),
      },
    ],
  };
}

async function readStdinJson() {
  let raw = "";
  for await (const chunk of process.stdin) {
    raw += chunk;
  }
  raw = raw.trim();
  if (!raw) {
    return {};
  }
  return JSON.parse(raw);
}

if (
  process.argv[1] &&
  fileURLToPath(import.meta.url) === resolve(process.argv[1])
) {
  const response = handleContentFactoryWorkerRequest(await readStdinJson());
  process.stdout.write(`${JSON.stringify(response)}\n`);
  if (response.status !== "completed") {
    process.exitCode = 1;
  }
}
