function hasStructuredOutputTool(body) {
  return requestToolNames(body).includes(STRUCTURED_OUTPUT_TOOL_NAME);
}

function requestToolNames(body) {
  if (!Array.isArray(body?.tools)) {
    return [];
  }

  return body.tools
    .map((tool) =>
      String(
        tool?.function?.name || tool?.name || tool?.tool?.function?.name || "",
      ).trim(),
    )
    .filter(Boolean);
}

function scriptedToolName(scripted) {
  return String(scripted?.name || scripted?.toolName || "").trim();
}

function scriptedToolRequired(scripted) {
  return scripted?.requireToolInRequest !== false;
}

function ensureScriptedToolAvailable(scripted, body) {
  if (scripted?.type !== "tool_call" || !scriptedToolRequired(scripted)) {
    return;
  }

  const expectedToolName = scriptedToolName(scripted);
  if (!expectedToolName) {
    throw new Error("scripted tool_call is missing name");
  }

  const tools = requestToolNames(body);
  if (!tools.includes(expectedToolName)) {
    const toolSummary = tools.length > 0 ? tools.join(", ") : "<none>";
    throw new Error(
      `scripted tool_call '${expectedToolName}' was requested, but provider request tools=${toolSummary}`,
    );
  }
}

function jsonResponse(response, statusCode, payload) {
  response.writeHead(statusCode, {
    "content-type": "application/json; charset=utf-8",
  });
  response.end(`${JSON.stringify(payload)}\n`);
}

function readJsonBody(request) {
  return new Promise((resolve, reject) => {
    const chunks = [];
    let totalBytes = 0;
    request.on("data", (chunk) => {
      totalBytes += chunk.length;
      if (totalBytes > 1_000_000) {
        reject(new Error("fixture request body exceeds 1MB"));
        request.destroy();
        return;
      }
      chunks.push(chunk);
    });
    request.on("error", reject);
    request.on("end", () => {
      const text = Buffer.concat(chunks).toString("utf8");
      if (!text.trim()) {
        resolve({});
        return;
      }
      try {
        resolve(JSON.parse(text));
      } catch (error) {
        reject(error);
      }
    });
  });
}

function buildChatChunk({
  id,
  model,
  content = "",
  finishReason = null,
  usage = null,
  delta = null,
}) {
  return {
    id,
    object: "chat.completion.chunk",
    created: 1_770_000_000,
    model,
    choices: [
      {
        index: 0,
        delta:
          delta ||
          (content
            ? {
                role: "assistant",
                content,
              }
            : {}),
        finish_reason: finishReason,
      },
    ],
    ...(usage ? { usage } : {}),
  };
}

function writeSse(response, payload) {
  response.write(
    `data: ${typeof payload === "string" ? payload : JSON.stringify(payload)}\n\n`,
  );
}

function sendStreamingChatCompletion(response, { model, content }) {
  const id = `chatcmpl-fixture-${Date.now()}`;
  response.writeHead(200, {
    "content-type": "text/event-stream; charset=utf-8",
    "cache-control": "no-cache",
    connection: "keep-alive",
  });
  writeSse(response, buildChatChunk({ id, model, content }));
  writeSse(
    response,
    buildChatChunk({
      id,
      model,
      finishReason: "stop",
      usage: {
        prompt_tokens: 1,
        completion_tokens: 1,
        total_tokens: 2,
      },
    }),
  );
  writeSse(response, "[DONE]");
  response.end();
}

function buildStructuredOutputToolCall() {
  return buildToolCall({
    idPrefix: "call-fixture-structured-output",
    name: STRUCTURED_OUTPUT_TOOL_NAME,
    arguments: buildStructuredOutputArguments(),
  });
}

function normalizeToolCallArguments(argumentsPayload) {
  if (typeof argumentsPayload === "string") {
    return argumentsPayload;
  }
  return JSON.stringify(argumentsPayload || {});
}

function buildToolCall({
  id,
  idPrefix = "call-fixture-tool",
  name,
  arguments: argumentsPayload,
}) {
  return {
    id: id || `${idPrefix}-${Date.now()}`,
    type: "function",
    function: {
      name,
      arguments: normalizeToolCallArguments(argumentsPayload),
    },
  };
}

function sendStreamingToolCall(response, { model, toolCall }) {
  const id = `chatcmpl-fixture-${Date.now()}`;
  response.writeHead(200, {
    "content-type": "text/event-stream; charset=utf-8",
    "cache-control": "no-cache",
    connection: "keep-alive",
  });
  writeSse(
    response,
    buildChatChunk({
      id,
      model,
      delta: {
        role: "assistant",
        tool_calls: [
          {
            index: 0,
            id: toolCall.id,
            type: "function",
            function: {
              name: toolCall.function.name,
              arguments: "",
            },
          },
        ],
      },
    }),
  );
  writeSse(
    response,
    buildChatChunk({
      id,
      model,
      delta: {
        tool_calls: [
          {
            index: 0,
            function: {
              arguments: toolCall.function.arguments,
            },
          },
        ],
      },
    }),
  );
  writeSse(
    response,
    buildChatChunk({
      id,
      model,
      finishReason: "tool_calls",
      usage: {
        prompt_tokens: 1,
        completion_tokens: 1,
        total_tokens: 2,
      },
    }),
  );
  writeSse(response, "[DONE]");
  response.end();
}

