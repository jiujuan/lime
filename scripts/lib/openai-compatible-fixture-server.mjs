import http from "node:http";

export const DEFAULT_FIXTURE_MODEL = "lime-fixture-chat";
export const DEFAULT_FIXTURE_API_KEY = "fixture-key";
const STRUCTURED_OUTPUT_TOOL_NAME = "StructuredOutput";

