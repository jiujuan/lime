function normalizeScriptedResponses(value) {
  if (!Array.isArray(value)) {
    return [];
  }
  return value
    .map((item) =>
      typeof item === "function" || (item && typeof item === "object")
        ? item
        : null,
    )
    .filter(Boolean);
}

function scriptedResponseAt(scriptedResponses, scriptedIndex) {
  if (scriptedResponses.length === 0) {
    return null;
  }
  return scriptedResponses[
    Math.min(scriptedIndex, scriptedResponses.length - 1)
  ];
}

async function resolveScriptedResponse(
  scriptedResponses,
  scriptedIndex,
  context,
) {
  const scripted = scriptedResponseAt(scriptedResponses, scriptedIndex);
  if (typeof scripted === "function") {
    return await scripted(context);
  }
  return scripted;
}

function shouldDeferScriptedResponse(scripted, body, options) {
  if (options.deferScriptedToolCallsUntilAvailable !== true) {
    return false;
  }

  const requestTools = requestToolNames(body);
  if (typeof scripted === "function") {
    return requestTools.length === 0;
  }

  if (scripted?.type !== "tool_call" || !scriptedToolRequired(scripted)) {
    return false;
  }

  return requestTools.length === 0;
}

function sendScriptedChatCompletion(
  response,
  { model, scripted, stream, body },
) {
  if (!scripted || typeof scripted !== "object") {
    return false;
  }

  if (scripted.type === "tool_call") {
    ensureScriptedToolAvailable(scripted, body);
    const toolCall = buildToolCall({
      id: scripted.id,
      idPrefix: scripted.idPrefix,
      name: scripted.name,
      arguments: scripted.arguments,
    });
    if (stream) {
      sendStreamingToolCall(response, { model, toolCall });
    } else {
      sendJsonToolCall(response, { model, toolCall });
    }
    return true;
  }

  if (scripted.type === "text" || typeof scripted.content === "string") {
    const content = String(scripted.content || "");
    if (stream) {
      sendStreamingChatCompletion(response, { model, content });
    } else {
      sendJsonChatCompletion(response, { model, content });
    }
    return true;
  }

  return false;
}

export async function startOpenAiCompatibleFixtureServer(options = {}) {
  const model =
    String(options.model || DEFAULT_FIXTURE_MODEL).trim() ||
    DEFAULT_FIXTURE_MODEL;
  const apiKey =
    String(options.apiKey || DEFAULT_FIXTURE_API_KEY).trim() ||
    DEFAULT_FIXTURE_API_KEY;
  const content = String(options.content || "MO_OK");
  const scriptedResponses = normalizeScriptedResponses(
    options.scriptedResponses,
  );
  const scriptedOptions = {
    deferScriptedToolCallsUntilAvailable:
      options.deferScriptedToolCallsUntilAvailable === true,
  };
  let scriptedIndex = 0;
  const requests = [];
  const connectionDiagnostics = [];
  const connectionDiagnosticsEnabled =
    options.connectionDiagnostics === true ||
    process.env.LIME_FIXTURE_CONNECTION_DIAGNOSTICS === "1";
  const socketIds = new Map();
  const socketRequestCounts = new Map();
  let nextSocketId = 1;
  let nextRequestId = 1;

  const socketSnapshot = (socket) => ({
    socketId: socketIds.get(socket) || null,
    localPort: socket.localPort || null,
    remotePort: socket.remotePort || null,
    destroyed: socket.destroyed,
    readableEnded: socket.readableEnded,
    writableEnded: socket.writableEnded,
    requestCount: socketRequestCounts.get(socket) || 0,
  });
  const recordConnectionDiagnostic = (event, details = {}) => {
    if (!connectionDiagnosticsEnabled) {
      return;
    }
    const entry = {
      at: new Date().toISOString(),
      event,
      ...details,
    };
    connectionDiagnostics.push(entry);
    console.error(`[fixture:connection] ${JSON.stringify(entry)}`);
  };

  const server = http.createServer(async (request, response) => {
    const url = new URL(request.url || "/", "http://127.0.0.1");
    const requestId = nextRequestId;
    nextRequestId += 1;
    const requestSocket = request.socket;
    socketRequestCounts.set(
      requestSocket,
      (socketRequestCounts.get(requestSocket) || 0) + 1,
    );
    const requestDetails = () => ({
      requestId,
      method: request.method || null,
      path: url.pathname,
      requestComplete: request.complete,
      responseFinished: response.writableFinished,
      ...socketSnapshot(requestSocket),
    });
    recordConnectionDiagnostic("request-start", requestDetails());
    request.once("end", () => {
      recordConnectionDiagnostic("request-end", requestDetails());
    });
    request.once("close", () => {
      recordConnectionDiagnostic("request-close", requestDetails());
    });
    response.once("finish", () => {
      recordConnectionDiagnostic("response-finish", requestDetails());
    });
    response.once("close", () => {
      recordConnectionDiagnostic("response-close", requestDetails());
    });
    let requestRecord = null;

    if (request.method === "GET" && url.pathname === "/v1/models") {
      jsonResponse(response, 200, {
        object: "list",
        data: [
          {
            id: model,
            object: "model",
            created: 1_770_000_000,
            owned_by: "lime-fixture",
          },
        ],
      });
      return;
    }

    if (request.method !== "POST" || url.pathname !== "/v1/chat/completions") {
      jsonResponse(response, 404, {
        error: {
          message: `fixture route not found: ${request.method} ${url.pathname}`,
          type: "not_found",
        },
      });
      return;
    }

    try {
      const body = await readJsonBody(request);
      requestRecord = {
        method: request.method,
        path: url.pathname,
        authorization: request.headers.authorization || null,
        body,
      };
      requests.push(requestRecord);
      const requestIndex = requests.length - 1;
      const responseModel = String(body?.model || model);
      const scriptedCandidate = scriptedResponseAt(
        scriptedResponses,
        scriptedIndex,
      );
      const shouldDefer = shouldDeferScriptedResponse(
        scriptedCandidate,
        body,
        scriptedOptions,
      );
      const scripted = shouldDefer
        ? null
        : await resolveScriptedResponse(scriptedResponses, scriptedIndex, {
            body,
            request,
            requests,
            requestIndex,
            scriptedIndex,
          });
      if (
        scripted &&
        sendScriptedChatCompletion(response, {
          model: responseModel,
          scripted,
          stream: body?.stream === true,
          body,
        })
      ) {
        requestRecord.responseKind = scripted.type || "scripted";
        requestRecord.responseToolName = scriptedToolName(scripted) || null;
        scriptedIndex += 1;
        return;
      }
      const shouldCallStructuredOutput = hasStructuredOutputTool(body);
      if (body?.stream === true && shouldCallStructuredOutput) {
        sendStreamingStructuredOutputToolCall(response, {
          model: responseModel,
        });
      } else if (body?.stream === true) {
        sendStreamingChatCompletion(response, {
          model: responseModel,
          content,
        });
      } else if (shouldCallStructuredOutput) {
        sendJsonStructuredOutputToolCall(response, {
          model: responseModel,
        });
      } else {
        sendJsonChatCompletion(response, {
          model: responseModel,
          content,
        });
      }
    } catch (error) {
      if (requestRecord) {
        requestRecord.responseError =
          error instanceof Error ? error.message : String(error);
      }
      jsonResponse(response, 400, {
        error: {
          message: error instanceof Error ? error.message : String(error),
          type: "invalid_request_error",
        },
      });
    }
  });
  server.on("connection", (socket) => {
    const socketId = nextSocketId;
    nextSocketId += 1;
    socketIds.set(socket, socketId);
    socketRequestCounts.set(socket, 0);
    recordConnectionDiagnostic("socket-open", socketSnapshot(socket));
    socket.once("end", () => {
      recordConnectionDiagnostic("socket-end", socketSnapshot(socket));
    });
    socket.once("close", (hadError) => {
      recordConnectionDiagnostic("socket-close", {
        ...socketSnapshot(socket),
        hadError,
      });
      socketIds.delete(socket);
      socketRequestCounts.delete(socket);
    });
  });
  server.keepAliveTimeout = 1_000;

  await new Promise((resolve, reject) => {
    server.once("error", reject);
    server.listen(0, "127.0.0.1", () => {
      server.off("error", reject);
      resolve();
    });
  });

  const address = server.address();
  if (!address || typeof address === "string") {
    server.close();
    throw new Error("fixture server failed to bind a TCP port");
  }

  const baseUrl = `http://127.0.0.1:${address.port}`;
  return {
    baseUrl,
    requests,
    connectionDiagnostics,
    provider: buildProviderDescriptor({ baseUrl, model, apiKey }),
    close: async () => {
      recordConnectionDiagnostic("server-close-start", {
        sockets: [...socketIds.keys()].map(socketSnapshot),
      });
      const closeResult = new Promise((resolve, reject) => {
        server.close((error) => {
          if (error) {
            reject(error);
            return;
          }
          resolve();
        });
        server.closeIdleConnections?.();
        recordConnectionDiagnostic("server-close-idle-requested", {
          sockets: [...socketIds.keys()].map(socketSnapshot),
        });
      });
      await new Promise((resolve) => setTimeout(resolve, 25));
      const unclaimedSockets = [...socketIds.keys()].filter(
        (socket) =>
          !socket.destroyed && (socketRequestCounts.get(socket) || 0) === 0,
      );
      // Node does not classify a TCP connection as HTTP-idle before its first request.
      // Such a socket cannot own a provider response body, so targeted cleanup cannot hide one.
      for (const socket of unclaimedSockets) {
        socket.destroy();
      }
      recordConnectionDiagnostic("server-close-unclaimed-destroyed", {
        sockets: unclaimedSockets.map(socketSnapshot),
      });
      await closeResult;
      recordConnectionDiagnostic("server-close-complete", {
        sockets: [...socketIds.keys()].map(socketSnapshot),
      });
    },
  };
}
