pub(crate) fn runtime_analysis_markdown_copy(
    locale: Option<&str>,
) -> &'static RuntimeAnalysisMarkdownCopy {
    match resolved_current_locale_family(locale) {
        RuntimeMarkdownLocaleFamily::ZhTw => &ZH_TW_ANALYSIS_COPY,
        RuntimeMarkdownLocaleFamily::Ja => &JA_JP_ANALYSIS_COPY,
        RuntimeMarkdownLocaleFamily::Ko => &KO_KR_ANALYSIS_COPY,
        RuntimeMarkdownLocaleFamily::En => &EN_US_ANALYSIS_COPY,
        RuntimeMarkdownLocaleFamily::ZhCn => &ZH_CN_ANALYSIS_COPY,
    }
}

