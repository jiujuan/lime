pub(crate) fn runtime_review_markdown_copy(
    locale: Option<&str>,
) -> &'static RuntimeReviewMarkdownCopy {
    match resolved_current_locale_family(locale) {
        RuntimeMarkdownLocaleFamily::ZhTw => &ZH_TW_REVIEW_COPY,
        RuntimeMarkdownLocaleFamily::Ja => &JA_JP_REVIEW_COPY,
        RuntimeMarkdownLocaleFamily::Ko => &KO_KR_REVIEW_COPY,
        RuntimeMarkdownLocaleFamily::En => &EN_US_REVIEW_COPY,
        RuntimeMarkdownLocaleFamily::ZhCn => &ZH_CN_REVIEW_COPY,
    }
}

